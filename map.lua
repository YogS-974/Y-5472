Map.collide_zones = {}

Map.load_collide_zones = function()
	Map.collide_zones = {}

	local cm = Map.niveaux[Map.current][1]
	local cl = Map.niveaux[Map.current][2]

	for i=1, #cm do
		for j=1, #cm[i] do
			if cm[i][j] == 245 then
				local cz = {}
				cz.x = (j - 1) * Map.WIDTH_TILE
				cz.y = (i - 1) * Map.HEIGHT_TILE
				cz.width = Map.WIDTH_TILE
				cz.height = Map.HEIGHT_TILE

				table.insert(Map.collide_zones, cz)
			end

			if cl[i][j] == 1 then
				local cz = {}
				cz.x = (j - 1) * Map.WIDTH_TILE
				cz.y = (i - 1) * Map.HEIGHT_TILE
				cz.width = Map.WIDTH_TILE
				cz.height = Map.HEIGHT_TILE

				table.insert(Map.collide_zones, cz)
			end
		end
	end
end

return Map